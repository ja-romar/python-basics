# Las siguientes son las operaciones basicas que podemos realizar en python

numero1 = 9 
numero2 = 4

result_suma = numero1 + numero2 #suma 
print(result_suma)

result_sub = numero1 - numero2 #Substracccion
print(result_sub)

result_mult = numero1 * numero2 #Multiplicar
print(result_mult)

result_div = numero1 / numero2 #Division
print(result_div)

result_fdiv = numero1 // numero2 # Division flotante ( a diferencia de la division normal regresa un entero)
print(result_fdiv)

result_mod = numero1 % numero2 # Regresa el remanente despues de una division